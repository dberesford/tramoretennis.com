<strong>Directions:</strong>
Tramore Tennis Club is located on Doneraile Drive in Love Lane, only 10 miles outside the port city of Waterford, and has beautiful views over Tramore bay.<br>
</p>
<p>
From Waterford:<br>
Entering Tramore from Waterford drive straight through the first two roundabouts and you will pass the Majestic Hotel on your right and Splashworld on your left. Continue straight through the mini roundabout at the Sands Hotel and up Galweys Hill. The entrance to Love Lane is on the left hand side, opposite the Church of Ireland and just beyond the Church Villa B&B. The club is located a few hundred yards into the lane at the bend of the road.
</p>
<p>
From Dungarvan:</br>
Entering Tramore from the coast road to Dungarvan you drive straight through the roundabout at the top of Newtown Hill. Drive down the hill and straight through the mini roundabout at the Daybreak shop and on to Cove Road. Take the next right turn into Love Lane. Follow the curve of the road, passed the Coastguard station, and up to the tennis club, which is located at the next bend in the road.
