<h2>Court Access Rules</h2>
<hr>
 
<p><b>General Rules</b></p>
<ul>
<li>Only paid up members of Tramore Tennis Club are permitted on the premises (except where green fees are paid by a non member). </li>
<li>An adult must accompany juniors under 10 at all times. </li>
<li>Proper tennis attire must be worn. </li>
<li>Only tennis to be played on the courts. </li>
<li>All members must abide by the Code of Conduct and Code of Ethics and Good Practice for Children in Sport. Copies of this are available in the Clubhouse.</li>
<li>Club Competitions and Events take priority. </li>
<li>Booking system in operation. Court times will vary depending on demand – however ladder matches are permitted to finish. </li>
</ul>
<p><b>Court Priority</b><br />
Juniors have priority during the following times:</p>
<ul><b>Winter Time</b> (September – June, includes School Mid-terms, Christmas and Easter)</p>
<li>Weekdays from 3pm-6pm.</li>
<li>Saturdays from 10am-6pm on 4 courts. One court is reserved for senior use.</li>
<li>Sundays (and Bank holidays) senior members have priority. </li>
</ul>
<ul><b>Summer holidays</b> (July &amp; August)</p>
<li>Monday-Saturday 10am-6pm  on 4 courts. Juniors are allocated courts, by the groundsperson, in 30 minute slots. One court is allocated to senior members during this time, by the groundsperson, in 1 hour slots. Senior members can only use additional courts if they are not required by junior members. If senior members are on an additional court,  they can be asked to move or vacate the court, if it is required by juniors.</li>
<li>Sundays (and Bank holidays) senior members have priority. </li>
</ul>
<p>At all other times senior members have priority. </p>
<p>Please be aware that if you are playing outside your allotted time you can be asked to vacate the court. </p>
<p>Junior members are permitted on the club premises after 6pm under the following exceptional circumstances:<br />
1)	If authorised to play on the senior ladder.<br />
2)	If a court is free they may play with their own parent. Note one parent per child.<br />
3)	Courts must NOT be booked with juniors are part of the booking.<br />
4)	Parent child combinations must give way to senior members immediately. </p>
<p>If this privilege is abused it will be withdrawn. During Senior Competitions juniors under 14 must vacate premises by 8pm.  </p>
<p><b>Booking system for senior times</b><br />
Twice a week a timesheet is placed on the external noticeboard at the clubhouse allowing courts to be booked on a first come first served basis. Please note that each member may only book 2 courts per timesheet. </p>
<ul>
<li>From Saturday at 5:30pm courts can be booked for Sunday, Monday, Tuesday and Wednesday evenings.</li>
<li>From Monday at 6pm courts can be booked for Thursday, Friday and Saturday evenings.</li>
</ul>
<p>Timeslots for courts 1, 2 and 4 last 1.5 hours and run from 6:30pm, 8pm and 9:30pm on Monday, Tuesday, Thursday and Friday. Timeslots on courts 3 and 5 are hourly from 6-10pm. On Wednesday evening an hourly system is in operation on all courts. </p>
<p>Please note that courts 1-4 are pre booked on Tuesday mornings from 10am-1pm for the Ladies American Tournament, except during school holidays. </p>
<p>During the summer months all courts are pre booked on Wednesday evenings from 7:30pm for the American Tournament. </p>
<p><b> Coaching </b><br />
The coaches affiliated with the club have access to a single court at certain pre agreed times when either group or individual coaching of members has been arranged. Pre booking for coaching has been permitted by the committee in senior time on a Friday evening from 6-7pm. </p>
<p><b> Hitting </b><br />
No court priority is given for hitting practice. The junior player will approach the grounds person on arrival and queue as usual for a 30-minute slot. If courts are busy and more than one junior member wishes to arrange a hit then the grounds person may double up two groups on the one court.</p>
<p><b>Green Fees </b><br />
All visitors are required to pay the following Green Fees:-</p>
<ul>
<li>Adults: €7 per hour</li>
<li>Juniors: €4 per hour</li>
</ul>
<p>Visitors may pay and play up to 6 times during the year.</p>
<p>If visitors are not accompanied by a club member,  they must sign the Visitors Book and pay the Green Fee to the grounds person on duty. If visitors are accompanied by a club member when the grounds person is not on duty,  the club member is responsible for ensuring the Visitors Book is signed.  The Green Fee should then be placed in a Green Fees envelope which can be found, along with further details, in the Visitors Book.</p>
<p><b>Court lights </b><br />
Tokens for lights are available for purchase from the office at the clubhouse or from Florries Off Licence, beside the Ritz Bar on Cove Road. </p>

